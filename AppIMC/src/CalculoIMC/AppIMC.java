    private void BotaoMulherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoMulherActionPerformed

        try {
            peso = Double.parseDouble(Peso.getText().replace(",", "."));
            altura = Double.parseDouble(Altura.getText().replace(",", "."));
            calculo = peso / (altura*altura);
            if (calculo < 19.1){ ResultadoIMC.setText("Imc: "+calculo+ " Voc√™ est√° abaixo do peso ");}// END IF
            else if (calculo >= 19.1 & calculo <= 25.8){ResultadoIMC.setText("IMC: "+calculo+ " voc√™ est√° no peso normal");}// END ELSE IF
            else if (calculo >= 25.9 & calculo <= 27.3){ResultadoIMC.setText("IMC: "+calculo+ " voc√™ est√° pouco acima do peso");}// END ELSE IF
            else if (calculo >= 27.4 & calculo <= 32.3){ResultadoIMC.setText("IMC: "+calculo+ " voc√™ est√° acima do peso");}// END ELSE IF
            else if (calculo >= 32.4) {ResultadoIMC.setText("Obesidade Extrema");}
            else {ResultadoIMC.setText("N√£o √© humano");}
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,"mensagem\nCausa: "+e.getMessage(),"t√≠tulo",JOptionPane.ERROR_MESSAGE);

        }// END catch
    }//GEN-LAST:event_BotaoMulherActionPerformed

